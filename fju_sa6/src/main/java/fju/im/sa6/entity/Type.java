public class Type {

	private String typeName;

	private int typeNum;

	public Type(String typeName, int typeNum) {
		this.typeName = typeName;
		this.typeNum = typeNum;
	}

	public String getTypeName() {
		return typeName;
	}

	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}

	public int getTypeNum() {
		return typeNum;
	}

	
	

}
